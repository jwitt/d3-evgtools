class Tx_D3Evgtools_Domain_Model_AccordionContent extends Tx_D3Evgtools_Domain_Model_Abstract {

	/**
	 * name
	 *
	 * @var string
	 */
	protected $name;

	/**
	 * Title Text
	 *
	 * @var string
	 */
	protected $title;

	/**
	 * content
	 *
	 * @var string
	 */
	protected $content;

	/**
	 * Returns the name
	 *
	 * @return string $name
	 */
	public function getName() {
		return $this->name;
	}

	/**
	 * Sets the name
	 *
	 * @param string $name
	 * @return void
	 */
	public function setName($name) {
		$this->name = $name;
	}

	/**
	 * Returns the title
	 *
	 * @return string $title
	 */
	public function getTitle() {
		return $this->title;
	}

	/**
	 * Sets the title
	 *
	 * @param string $title
	 * @return void
	 */
	public function setTitle($title) {
		$this->title = $title;
	}

	/**
	 * Returns the content
	 *
	 * @return string $content
	 */
	public function getContent() {
		return $this->content;
	}

	/**
	 * Sets the content
	 *
	 * @param string $content
	 * @return void
	 */
	public function setContent($content) {
		$this->content = $content;
	}

}
